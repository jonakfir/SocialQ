        <div>
          <h3 class="font-semibold text-white mb-1">How do I cancel my subscription?</h3>
          <p class="text-gray-300 leading-relaxed">
            Subscriptions are managed through Apple. Go to <strong>Settings → Apple ID → Subscriptions</strong>
            on your iPhone or iPad, find AboutFace, and tap Cancel Subscription.
          </p>
        </div>

        <div>
          <h3 class="font-semibold text-white mb-1">How do I delete my account?</h3>
          <p class="text-gray-300 leading-relaxed">
            Open the app, go to the <strong>Profile</strong> tab, scroll to the bottom, and tap
            <strong>Delete Account</strong>. This permanently removes your account and all associated data.
            You can also email us at support@social-q.net to request deletion.
          </p>
        </div>

        <div>
          <h3 class="font-semibold text-white mb-1">The app isn't recognizing my face. What can I do?</h3>
          <p class="text-gray-300 leading-relaxed">
            Make sure you are in a well-lit environment and that your face is fully visible in the
            camera frame. The app works best with even, front-facing lighting. If the issue persists,
            try restarting the app.
          </p>
        </div>

        <div>
          <h3 class="font-semibold text-white mb-1">I purchased a subscription but the app still shows the paywall.</h3>
          <p class="text-gray-300 leading-relaxed">
            Try logging out and back into your account in the app. If that doesn't work, go to
            <strong>Settings → Apple ID → Subscriptions</strong> to confirm the purchase went through,
            then contact us at support@social-q.net with your Apple ID email and we'll sort it out.
          </p>
        </div>

        <div>
          <h3 class="font-semibold text-white mb-1">How do I reset my password?</h3>
          <p class="text-gray-300 leading-relaxed">
            On the Login screen, tap <strong>Forgot Password</strong>, or email us at
            support@social-q.net and we'll send you a reset link.
          </p>
        </div>

        <div>
          <h3 class="font-semibold text-white mb-1">Is my face data stored?</h3>
          <p class="text-gray-300 leading-relaxed">
            During gameplay, facial analysis runs in real time and is not stored. Only your game
            scores are sent to our servers. Photos you explicitly upload are stored and can be
            deleted at any time. See our <a href="/privacy" class="text-blue-400 underline">Privacy Policy</a> for full details.
          </p>
